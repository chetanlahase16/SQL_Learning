DROP TABLE IF EXISTS addresses;

CREATE TABLE addresses(
id INT AUTO_INCREMENT PRIMARY KEY,
user_id INT,
street VARCHAR(255),
city VARCHAR(100),
state VARCHAR(100),
pincode VARCHAR(100),
CONSTRAINT fk_users FOREIGN KEY(user_id) REFERENCES users(id) ON DELETE CASCADE   -- on delete cascade is for if in users table anyone deletes any person then in address table the record which references to that users table record will be also delete

-- we can also set delete record to null by using ON DELETE SET NULL

);

